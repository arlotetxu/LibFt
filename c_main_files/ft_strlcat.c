#include "libft.h"
#include <stdio.h>

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;

	/* getting the dst length reaching a NULL value or dstsize value */
	while (dst[i] != '\0' && i < dstsize)
		i++;
	printf("El valor de i (destino) es: %lu\n", i);

	while (src[j] != '\0' && (i + j + 1) < dstsize)
	{
		dst[i + j] = src[j];
		j++;
	}
	if (i < dstsize)
		dst[i + j] = '\0';
	return (i + ft_strlen(src));
}
